package com.revature.ers.servlets;

import java.io.IOException;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

public class MainServlet extends HttpServlet {
    // i wanted to maintain a single front servlet,
    // but i needed a simple servlet to just respond to posts
    // without needing to redirect
    // so i could play with ajax more

    protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        request.getRequestDispatcher(Helper.process(request)).forward(request, response);
        System.out.println("======LOG - doGET called " + request);

    }

    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        request.getRequestDispatcher(Helper.process(request)).forward(request, response);
        System.out.println("======LOG - doPOST called " + request);
    }

    protected void doPut(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        request.getRequestDispatcher(Helper.process(request)).forward(request, response);
        System.out.println("======LOG - doPUT called " + request);
    }

}
